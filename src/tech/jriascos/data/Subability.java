package tech.jriascos.data;

public class Subability {
    private String name;
    private String url;

    public Subability(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return this.name;
    }

    public String getUrl() {
        return this.url;
    }
}
